"""
Завдання 1: Замикання та кешування чисел Фібоначчі

Реалізація функції caching_fibonacci, яка створює та використовує кеш 
для зберігання і повторного використання вже обчислених значень чисел Фібоначчі.
"""


def caching_fibonacci():

    # Створюємо порожній словник для зберігання обчислених значень
    cache = {}
    
    def fibonacci(n):
     
        # Базові випадки
        if n <= 0:
            return 0
        if n == 1:
            return 1
        
        # Якщо значення вже є у кеші, повертаємо його
        if n in cache:
            return cache[n]
        
        # Обчислюємо значення рекурсивно та зберігаємо у кеші
        cache[n] = fibonacci(n - 1) + fibonacci(n - 2)
        return cache[n]

    return fibonacci

